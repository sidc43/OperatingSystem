void draw_char(uint32_t x, uint32_t y, char ch, uint32_t fg, uint32_t bg) {
    uint32_t cp = (uint8_t)ch;
    if (cp < FONT_FIRST || cp > FONT_LAST) cp = '?';

    const uint8_t* glyph = font8x16 + (cp - FONT_FIRST) * FONT_H;

    for (uint32_t row = 0; row < FONT_H; ++row) {
        uint8_t bits = glyph[row];
        for (uint32_t col = 0; col < FONT_W; ++col) {
            bool set = (bits >> (7u - col)) & 1u;
            draw_pixel(x + col, y + row, set ? fg : bg);
        }
    }
}

void draw_text(uint32_t x, uint32_t y, const char* s, uint32_t fg, uint32_t bg) {
    draw_text_ex(x, y, s, fg, bg);
}

uint32_t draw_text_ex(uint32_t x, uint32_t y, const char* s,
                      uint32_t fg, uint32_t bg) {
    while (*s) {
        draw_char(x, y, *s++, fg, bg);
        x += FONT_W;
    }
    return x;
}

void draw_char_into(uint32_t* fb, uint32_t fb_w,
                    uint32_t x, uint32_t y, char c,
                    uint32_t fg, uint32_t bg) {
    uint32_t code = (uint32_t)(uint8_t)c;
    if (code < FONT_FIRST || code > FONT_LAST) code = (uint32_t)' ';
    const uint8_t* glyph = &font8x16[(code - FONT_FIRST) * FONT_H];
    for (uint32_t row = 0; row < FONT_H; ++row) {
        uint8_t bits = glyph[row];
        for (uint32_t col = 0; col < FONT_W; ++col)
            fb[(y + row) * fb_w + (x + col)] =
                (bits & (0x80u >> col)) ? fg : bg;
    }
}

